<h1>Cryptonite Version 1.3</h1>
<h2>About</h2>
<p>Cryptonite is a peer-to-peer, structureless and OpenSSL end-to-end encrypted text messager written in pure PHP. The main user is the admin account, which has all privileges on the messager system. It is used to prevent access to the messager from outside.</p>
<p>The messager features many functions like a headserver less update/external keypair generation, exploit protection and a client mode for local server sharing.</p>
<h2>Basic Usage</h2>
<p>For the basic usage of cryptonite you need to have at least one valid server installation.</p>
<p>Step by step walkthrough:</p>
<p><span class="glyphicon glyphicon-chevron-right" aria-hidden="true"></span> get the target's (local) IP or URL and visit it using any browser.</p>
<p><span class="glyphicon glyphicon-chevron-right" aria-hidden="true"></span> copy it and visit "localhost/cryptonite"</p>
<p><span class="glyphicon glyphicon-chevron-right" aria-hidden="true"></span> log in the form or register if using a new install</p>
<p><span class="glyphicon glyphicon-chevron-right" aria-hidden="true"></span> go back to the target URL and press on "sharekey"</p>
<p><span class="glyphicon glyphicon-chevron-right" aria-hidden="true"></span> use this file and URL to crypt messages to your target</p>
